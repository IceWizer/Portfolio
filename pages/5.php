<h1>Portfolio</h1>
<h2>Déployer un service</h2>

<p>Pour déployer mon portfolio, ce site, j'utilise github actions.</p>
<p>Le principe est simple, à chaque fois que je fais un push sur mon dépôt, github actions va exécuter un script qui va déployer le site sur mon serveur.</p>
<p>Il va également envoyer un mail à mon adresse pour me prévenir que le site a été déployé.</p>
<p>Cela demande du temps pour "perfectionner" le script, mais fait gagner beaucoup de temps lors du déployement.</p>