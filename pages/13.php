<h1>Plannigo</h1>
<h2>Participer à la valorisation de l’image de l’organisation sur les médias numériques en tenant compte du cadre juridique et des enjeux économiques</h2>

<p>Plannigo est une application web qui permet la gestion de planning.</p>
<p>Chaque fonctionnalité doit être fonctionnel sans bug, mais aussi sans faille de sécurité. Il est important que personne ne puisse accéder au données dont il ne doivent pas avoir accès.</p>
<p>Il faut donc faire attention lorsque l'on développe à ne pas faire accéder à des ressources dont l'utilisateur n'a pas accès.</p>
