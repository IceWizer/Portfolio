<h2>Gérer son identité professionnelle</h2>

<p>Linkedin est un réseau social à usage professionnel principalement. Il permet de trouver plus facilement des entreprises, ou de futurs employés.</p>
<p>Il permet également de se faire connaître, et de se faire contacter par des entreprises.</p>
<p>J'ai créé mon compte le 13 janvier 2022</p>
<p>Ce compte me permet de voir les actualités des entreprises facilement, ainsi je peux cibler les entreprises qui m'intéressent et y postuler.</p>
