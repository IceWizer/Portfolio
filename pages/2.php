<h1>Mégacasting</h1>
<h2>Participer à l’évolution d’un site Web exploitant les données de l’organisation</h2>

<p>Le site web de Mégacasting est un site web basé sur VueJs, il est donc possible de faire des requêtes HTTP pour récupérer des données, ou en envoyer.</p>
<p>Les services de Mégacasting utilisent les données de l'organisations, stockées en base de données, accessibles via une API du service Symfony et API platform (bundle de Symfony).</p>
<p>Le site web de Mégacasting est donc un client de l'API de Mégacasting, il peut donc récupérer les données de l'API, et les afficher sur le site web.</p>
