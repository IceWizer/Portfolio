<h2>Traiter des demandes concernant les applications</h2>

<p>L’application web permet la gestion de la mise en relation de personnels médicosociaux avec des structures afin de réaliser des interventions.</p>
<p>J'ai participé à une réunion avec le client, afin d'avoir ses retours sur l'application en cours de développement et les fonctionnalités déjà intégrer.</p>
<p>Le client a pu nous faire part de ses remarques, et nous avons pu les mettre en place pour que l'application convienne le mieux à ses attentes. Il nous a demandé d'ajouter une couleur différente pour chaque service ou encore remplacer certaine phrase.</p>
